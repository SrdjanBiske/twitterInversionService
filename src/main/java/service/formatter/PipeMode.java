package service.formatter;

/**
 * Modes for the Pipe class.
 * Two possible options: training
 * and testing.
 */
public enum PipeMode {

    /**
     * Used when the pipe is executed in a
     * training mode.
     */
    TRAINING,

    /**
     * Used when the pipe is executed in a
     * testing mode.
     */
    TESTING

}
